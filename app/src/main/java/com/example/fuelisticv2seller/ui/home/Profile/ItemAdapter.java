package com.example.fuelisticv2seller.ui.home.Profile;

public class ItemAdapter
{
    private int img;
    private String inputDesc;
    private String inputValue;

    public void setImg(int image)
    {
        this.img = image;
    }
    public int getImg()
    {
        return img;
    }
    public void setInputDesc(String input)
    {
        this.inputDesc = input;
    }
    public String getInputDesc()
    {
        return inputDesc;
    }
    public void setInputValue(String inputval)
    {
        this.inputValue = inputval;
    }

    public String getInputValue()
    {
        return inputValue;
    }
}
